    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        // TODO add your handling code here:
        
        config config = new config();

String sql =
    "SELECT r_id, r_fname, r_lname, r_email, r_type " +
    "FROM tbl_register " +
    "WHERE r_email = ? AND r_password = ? AND r_status = 'ACTIVE'";

try (Connection conn = config.connectDB();
     PreparedStatement pst = conn.prepareStatement(sql)) {

    pst.setString(1, l_email.getText());
    pst.setString(2, l_pass.getText());

    ResultSet rs = pst.executeQuery();

    if (rs.next()) {

    session.userId = rs.getInt("r_id");   // ‚≠ê THIS LINE
    session.userType = rs.getString("r_type").trim();

    JOptionPane.showMessageDialog(null, "Login Success!");

    new user_dashboard().setVisible(true);
    this.dispose();
    } else {
        JOptionPane.showMessageDialog(null, "Invalid Credentials");
    }

} catch (Exception e) {
    e.printStackTrace();
}
    }//GEN-LAST:event_loginActionPerformed

